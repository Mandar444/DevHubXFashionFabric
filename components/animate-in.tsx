"use client"

import React, { useEffect, useRef, useState } from "react"

interface AnimateInProps {
  children: React.ReactNode
  className?: string
  direction?: "up" | "down" | "left" | "right"
  delay?: number
  duration?: number
  once?: boolean
  style?: React.CSSProperties
}

// Optimized AnimateIn using CSS transitions + Intersection Observer
// This is much more performant than framer-motion for simple scroll animations
export function AnimateIn({
  children,
  className = "",
  direction = "up",
  delay = 0,
  duration = 0.5,
  once = true,
  style,
  ...props
}: AnimateInProps) {
  const ref = useRef<HTMLDivElement>(null)
  const [isVisible, setIsVisible] = useState(false)

  useEffect(() => {
    // Check for mobile to avoid hidden content issues
    const isMobile = window.innerWidth < 768;
    if (isMobile) {
      setIsVisible(true);
      return;
    }

    const element = ref.current
    if (!element) return

    const observer = new IntersectionObserver(
      ([entry]) => {
        if (entry.isIntersecting) {
          setIsVisible(true)
          if (once) {
            observer.unobserve(element)
          }
        } else if (!once) {
          setIsVisible(false)
        }
      },
      { threshold: 0.01, rootMargin: "100px" }
    )

    // Fail-safe: if observer doesn't fire in 1s, show content
    const timer = setTimeout(() => setIsVisible(true), 1000);

    observer.observe(element)
    return () => {
      observer.disconnect();
      clearTimeout(timer);
    }
  }, [once])

  const getTransform = () => {
    if (isVisible) return "translate3d(0, 0, 0)"
    switch (direction) {
      case "up": return "translate3d(0, 20px, 0)"
      case "down": return "translate3d(0, -20px, 0)"
      case "left": return "translate3d(20px, 0, 0)"
      case "right": return "translate3d(-20px, 0, 0)"
      default: return "translate3d(0, 0, 0)"
    }
  }

  return (
    <div
      ref={ref}
      className={className}
      style={{
        ...style,
        opacity: isVisible ? 1 : 0,
        transform: getTransform(),
        transition: `opacity ${duration}s ease-out ${delay}s, transform ${duration}s ease-out ${delay}s`,
        willChange: isVisible ? "auto" : "opacity, transform",
      }}
      {...props}
    >
      {children}
    </div>
  )
}

export function AnimateInStagger({
  children,
  className = "",
  direction = "up",
  staggerDelay = 0.1,
  duration = 0.5,
  once = true,
  style,
  ...props
}: AnimateInProps & { staggerDelay?: number }) {
  const ref = useRef<HTMLDivElement>(null)
  const [isVisible, setIsVisible] = useState(false)

  useEffect(() => {
    // Check for mobile to avoid hidden content issues
    const isMobile = typeof window !== 'undefined' && window.innerWidth < 768;
    if (isMobile) {
      setIsVisible(true);
      return;
    }

    const element = ref.current
    if (!element) return

    const observer = new IntersectionObserver(
      ([entry]) => {
        if (entry.isIntersecting) {
          setIsVisible(true)
          if (once) {
            observer.unobserve(element)
          }
        } else if (!once) {
          setIsVisible(false)
        }
      },
      { threshold: 0.01, rootMargin: "200px" }
    )

    // Fail-safe: if observer doesn't fire in 1.5s, show content anyway
    const timer = setTimeout(() => setIsVisible(true), 1500);

    observer.observe(element)
    return () => {
      observer.disconnect();
      clearTimeout(timer);
    }
  }, [once])

  const getTransform = (visible: boolean) => {
    if (visible) return "translate3d(0, 0, 0)"
    switch (direction) {
      case "up": return "translate3d(0, 20px, 0)"
      case "down": return "translate3d(0, -20px, 0)"
      case "left": return "translate3d(20px, 0, 0)"
      case "right": return "translate3d(-20px, 0, 0)"
      default: return "translate3d(0, 0, 0)"
    }
  }

  return (
    <div ref={ref} className={className} style={style} {...props}>
      {React.Children.map(children, (child, i) => (
        <div
          key={i}
          className="h-full w-full"
          style={{
            opacity: isVisible ? 1 : 0,
            transform: getTransform(isVisible),
            transition: `opacity ${duration}s ease-out ${i * staggerDelay}s, transform ${duration}s ease-out ${i * staggerDelay}s`,
            willChange: isVisible ? "transform, opacity" : "auto",
            display: 'flex',
            flexDirection: 'column'
          }}
        >
          {child}
        </div>
      ))}
    </div>
  )
}
